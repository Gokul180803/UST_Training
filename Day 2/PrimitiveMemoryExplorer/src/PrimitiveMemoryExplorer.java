public class PrimitiveMemoryExplorer {
    public static void main(String[] args) {
        System.out.println("--- byte ---");
        System.out.println("Size in bits: " + Byte.SIZE);
        System.out.println("Size in bytes: " + Byte.BYTES + "\n");

        System.out.println("--- short ---");
        System.out.println("Size in bits: " + Short.SIZE);
        System.out.println("Size in bytes: " + Short.BYTES + "\n");

        System.out.println("--- int ---");
        System.out.println("Size in bits: " + Integer.SIZE);
        System.out.println("Size in bytes: " + Integer.BYTES + "\n");

        System.out.println("--- long ---");
        System.out.println("Size in bits: " + Long.SIZE);
        System.out.println("Size in bytes: " + Long.BYTES + "\n");

        System.out.println("--- float ---");
        System.out.println("Size in bits: " + Float.SIZE);
        System.out.println("Size in bytes: " + Float.BYTES + "\n");

        System.out.println("--- double ---");
        System.out.println("Size in bits: " + Double.SIZE);
        System.out.println("Size in bytes: " + Double.BYTES + "\n");

        System.out.println("--- char ---");
        System.out.println("Size in bits: " + Character.SIZE);
        System.out.println("Size in bytes: " + Character.BYTES + "\n");
    }
}
